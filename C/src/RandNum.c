/********************************************************************* 
 * Data de criacao    : 02/10/2020                                   *
 * Data de modificaco : 00/00/0000                                   *
 *-------------------------------------------------------------------*
 * makeMatrixAndVector : gera o matriz e vetor x com numeros         *
 * aletorios entre -100.0 e 100.0                                    * 
 *-------------------------------------------------------------------* 
 * Parametros de entrada:                                            * 
 *-------------------------------------------------------------------*
 * a - nao definido                                                  *
 * x - nao definido                                                  *
 * n - numero de elementos                                           *  
 *-------------------------------------------------------------------* 
 * Parametros de saida:                                              * 
 *-------------------------------------------------------------------* 
 * a - matriz                                                        *
 * x - vetor x                                                       *
 *-------------------------------------------------------------------* 
 * OBS:                                                              * 
 *-------------------------------------------------------------------* 
 *********************************************************************/
void makeMatrixAndVector(double *a, double *x, int const n) {

  int i, j;
  time_t t;

  srand( (unsigned int) time(&t));

  for (i = 0; i < n; i++) {
    x[i] = randNumb();
    for (j = 0; j < n; j++) {     
      a[i *n + j] = randNumb();
    }
  }

}

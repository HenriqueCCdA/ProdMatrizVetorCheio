def matvec(a, x, transpose: bool = False):
    '''
    ***************************************************************************
    * Data de criacao    : 10/10/2020                                         *
    * Data de modificaco : 00/00/2020                                         *
    *-------------------------------------------------------------------------*
    * MATVECFULL : operacao matriz vetor para matriz cheias                   *
    *-------------------------------------------------------------------------*
    * Parametros de entrada:                                                  *
    *-------------------------------------------------------------------------*
    * a  - vetor a                                                            *
    * x  - vetor b                                                            *
    * y  - indefinido                                                         *
    *-------------------------------------------------------------------------*
    * Parametros de saida:                                                    *
    *-------------------------------------------------------------------------*
    * y  - produto vetoria entre a e b                                        *
    *-------------------------------------------------------------------------*
    * OBS:                                                                    *
    *-------------------------------------------------------------------------*
    ***************************************************************************
    '''
    # matriz a transposta
    if(transpose):
        y = np.dot(a.T, x)
    # .........................................................................

    #
    else:
        y = np.dot(a, x)
    # .........................................................................
    return y
